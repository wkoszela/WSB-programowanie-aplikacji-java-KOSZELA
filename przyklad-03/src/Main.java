public class Main {

    public static void main(String[] args) {
        int age = 19;

        if (age > 18) {
            System.out.println("You are an adult");
        } else if (age > 16) {
            System.out.println("You are a teenager");
        } else {
            System.out.println("You are a child");
        }

        System.out.println("You are a human");
    }
}
